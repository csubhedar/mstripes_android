        public void DownloadShapeFile(String locationurl)
        {
            File SDCardRoot=getExternalCacheDir();
            File directory=new File(SDCardRoot.getAbsolutePath(),"/Mstripes");
            if(!directory.exists())
            {
                directory.mkdir();

            }
            try {
                InputStream input = null;
                try {
                    URL url = new URL(locationurl);
                    URLConnection conn=url.openConnection();
                    conn.setConnectTimeout(5000);
                    input = conn.getInputStream();
                    if (input==null)
                    { return;}
                    OutputStream output = new FileOutputStream(new File(directory.getAbsolutePath(), "/map.zip"));
                    Log.v(TAG,directory.getAbsolutePath());
                    try {
                        byte[] buffer = new byte[1024];
                        int bytesRead = 0;
                        while ((bytesRead = input.read(buffer, 0, buffer.length)) >= 0) {
                            output.write(buffer, 0, bytesRead);
                        }
                        mDownload=true;
                        output.close();

                    } catch (Exception exception) {
                        output.close();
                        mDownload=false;
                        Log.v(TAG,exception.getMessage());
                        exception.printStackTrace();
                    }

                } catch (Exception e) {
                    Log.v(TAG,e.getMessage());
                    e.printStackTrace();

                } finally {
                    if(input!=null)
                        input.close();
                    }
                }
                catch(Exception e)
                {Log.v(TAG,e.getMessage()+" reds");}
            return;
        }

        public boolean Unzip()
        {
            File SDCardRoot=getExternalCacheDir();
            String path=SDCardRoot.getAbsolutePath()+"/Mstripes";
            String filename="/map.zip";
            InputStream is;
            ZipInputStream zis;

            try {
                is=new FileInputStream(path+filename);
                zis=new ZipInputStream(new BufferedInputStream(is));
                ZipEntry ze;

                while((ze=zis.getNextEntry())!=null)
                {
                    ByteArrayOutputStream baos=new ByteArrayOutputStream();
                    byte[] buffer=new byte[1024];
                    int count;

                    String fname=ze.getName();
                    fileList.addFile(fname);
                    FileOutputStream fout=new FileOutputStream(path + fname);

                    while((count = zis.read(buffer))!=-1)
                    {
                        baos.write(buffer,0,count);
                        byte[] bytes= baos.toByteArray();
                        fout.write(bytes);
                        baos.reset();
                    }

                    fout.close();
                    zis.closeEntry();
                }
                zis.close();
            } catch (FileNotFoundException e) {
                e.printStackTrace();
                return false;
            } catch (IOException e) {
                e.printStackTrace();
                return false;
            }
                return true;

        }

